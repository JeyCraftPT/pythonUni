import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #arredondar ao inteiro mais perto 
print(math.ceil(pi)) #arrendondar para cima
print(math.floor(pi)) #arredondar para baixo
print(abs(pi)) #valor absoluto, basicamente módulo
print(pow(pi, 2)) #elevar do valor da esqueda ao valor da direita neste caso, pi^2
print(math.sqrt(pi)) #raiz quadrada
print(max(x, y, z)) #o máximo de um grupo de números
print(min(x, y, z)) #mínimo do grupo